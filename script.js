/**
 * Create a Backpack object.
 */

class Book {
    constructor(
        title,
        author,
        pages,
        headings,
        footer,
        theme,

    ){
        this.title=title;
        this.author=author;
        this.pages=pages;
        this.headings=headings;
        this.footer=footer;
        this.theme=theme;
    }


}
export default Book;